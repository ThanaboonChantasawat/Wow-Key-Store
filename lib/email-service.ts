export interface SendEmailParams {
  to: string;
  template: keyof typeof EMAIL_TEMPLATES;
  data: Record<string, any>;
}

export async function sendEmail({ to, template, data }: SendEmailParams) {
  try {
    if (!process.env.RESEND_API_KEY || !resend) {
      console.warn('‚ö†Ô∏è RESEND_API_KEY not configured, skipping email');
      return { success: false, message: 'Email service not configured' };
    }

    const emailTemplate = EMAIL_TEMPLATES[template];
    if (!emailTemplate) {
      throw new Error(`Email template '${template}' not found`);
    }

    const { data: emailData, error } = await resend.emails.send({
      from: FROM_EMAIL,
      to: [to],
      subject: emailTemplate.subject,
      html: emailTemplate.getHtml(data as any), // Cast to any to allow flexible data
    });

    if (error) {
      console.error('‚ùå Error sending email:', error);
      return { success: false, error };
    }

    console.log('‚úÖ Email sent successfully:', emailData?.id);
    return { success: true, data: emailData };
  } catch (error) {
    console.error('‚ùå Error sending email:', error);
    return { success: false, error };
  }
}

// Send bulk emails
export async function sendBulkEmails(emails: SendEmailParams[]) {
  const results = await Promise.allSettled(
    emails.map(email => sendEmail(email))
  );
  
  return results;
}
